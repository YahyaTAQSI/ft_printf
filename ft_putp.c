#include "ft_printf.h"

void	ft_putp(unsigned long long nb, int *cp)
{
	char	*x;

	x = "0123456789abcdef";
	if (nb >= 16)
		ft_putp(nb / 16, cp);
	ft_putchar(x[nb % 16], cp);
}
